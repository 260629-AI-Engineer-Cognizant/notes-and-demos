# Functional Model
# This is a different way to do custom neural networks with more advanced arch

# Create some fake classification data
X, y = make_classification(
    n_samples=1600,
    n_features = 8,
    n_informative =5,
    n_redundant = 1,
    n_repeated = 0,
    n_classes = 2,
    class_sep = 1.2,
    random_state=42
)

# Preprocess our data
X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=.2, random_state=42, stratify=y
)

# Scale our data
scaler = StandardScaler()
X_train = scaler.fit_transform(X_train)
X_test = scaler.transform(X_test)

"""
Build a functional model
You'll usually see this in an actual function itself
and the function returns the actual model
"""

def build_functional_model(input_dim: int) -> tf.keras.Model:
  # Input layer
  inputs = tf.keras.Input(shape = (input_dim,), name="customer_features")

  # Main Branch
  main = tf.keras.layers.Dense(32, activation="relu", name="dense_main_1")(inputs)
  main = tf.keras.layers.Dense(16, activation="relu", name="dense_main_2")(main)

  # Skip Branch
  skip = tf.keras.layers.Dense(16, activation="relu", name="skip_projection")(inputs)

  # Merge our branches
  merged = tf.keras.layers.Concatenate(name="merge_branches")([main, skip])
  hidden = tf.keras.layers.Dense(16, activation="relu", name="hidden_after_merge")(merged)
  outputs = tf.keras.layers.Dense(1, activation="sigmoid", name="churn_probability")(hidden)

  # Compile
  model = tf.keras.Model(inputs=inputs, outputs=outputs, name="fictional_churn_model")
  model.compile(
      optimizer = tf.keras.optimizers.Adam(learning_rate=.001),
      loss="binary_crossentropy",
      metrics = ["accuracy"]
  )
  # Return
  return model

model = build_functional_model(X_train.shape[1])
model.summary()

plot_path = "functional_model_graph.png"

try:
    tf.keras.utils.plot_model(
        model,
        to_file=plot_path,
        show_shapes=True,
        show_layer_names=True,
        expand_nested=False,
        dpi=110,
    )
    display(Image(filename=plot_path))
except Exception as exc:
    print("Could not create plot_model image in this runtime.")
    print("Model summary above still shows the graph connections.")
    print("Error:", exc)

logdir = "logs/functional_model"
shutil.rmtree(logdir, ignore_errors=True)

callbacks = [
    tf.keras.callbacks.TensorBoard(log_dir=logdir, write_graph=True),
    tf.keras.callbacks.EarlyStopping(monitor="val_loss", patience=3, restore_best_weights=True),
]

history = model.fit(
    X_train,
    y_train,
    validation_split=0.2,
    epochs=12,
    batch_size=32,
    callbacks=callbacks,
    verbose=1,
)

test_loss, test_acc = model.evaluate(X_test, y_test, verbose=0)
print(f"Test loss: {test_loss:.4f}")
print(f"Test accuracy: {test_acc:.4f}")

save_dir = Path("saved_models")
save_dir.mkdir(exist_ok=True)
keras_path = save_dir / "functional_churn_model.keras"

model.save(keras_path)
print("Saved model to:", keras_path)

loaded_model = tf.keras.models.load_model(keras_path)
print("Loaded model:", loaded_model.name)

original_preds = model.predict(X_test[:5], verbose=0)
loaded_preds = loaded_model.predict(X_test[:5], verbose=0)

comparison = pd.DataFrame({
    "original_prediction": original_preds.ravel(),
    "loaded_prediction": loaded_preds.ravel(),
    "difference": np.abs(original_preds.ravel() - loaded_preds.ravel()),
})

display(comparison)
print("Max prediction difference:", comparison["difference"].max())
print(os.path.getsize(keras_path))