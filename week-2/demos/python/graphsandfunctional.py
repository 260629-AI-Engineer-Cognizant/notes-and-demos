import os
import shutil
from pathlib import Path

import numpy as np
import pandas as pd
import tensorflow as tf

from IPython.display import display, Image
from sklearn.datasets import make_classification
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler

np.random.seed(42)
tf.random.set_seed(42)

# Eager Execution
# This is what happens now
# TF behaves like python and completes the operation the second you ask for it

x = tf.constant([[1.0, 2.0], [3.0, 4.0]], name="x")
w = tf.constant([[.5], [1.5]], name="w")
b = tf.constant([.25], name="b")

# Standard activation in a hidden neuron
eager_output = tf.nn.relu(tf.matmul(x, w) + b)

print("Eager Result: \n", eager_output.numpy())

""" You can still create a manual TF graph these days
this is used for custom training flows or steps
but we'll create a simple function that does the same thing as above
"""

# To turn this into a graph we need a decorator on the function
@tf.function
def tiny_graph(x, w, b):
  # Matrix Multiply -> add bias -> ReLU
  z = tf.matmul(x, w, name="matrix_multiply")
  z = tf.add(z, b, name="add_bias")
  return tf.nn.relu(z, name="relu_activation")

# Graph doesn't get made until you call the function the first time
# graph_output = tiny_graph(x, w, b)
# print("Graph result: \n", graph_output.numpy())

# Get our concrete function
concrete_fn = tiny_graph.get_concrete_function(x, w, b)

# This will give us our list of operations
for op in concrete_fn.graph.get_operations():
  print(f"{op.name:35s} | {op.type}")

"""
At this point we want to take a look at the visual component of the graph under
the hood, but we need to tell TF to watch for it before we can put in a log

This is just for visualization, not explicitly necessary
"""

logdir = "logs/tiny_graph"
shutil.rmtree(logdir, ignore_errors=True)

# Create a writer to write the graph itself

writer = tf.summary.create_file_writer(logdir)

with writer.as_default():
  # Turn on the TRACE so TF can view the graph and record it
  tf.summary.trace_on(graph=True, profiler=False)

  # Call the function for the first time
  _ = tiny_graph(x, w, b)

  tf.summary.trace_export(
      name="tiny_graph_trace",
      step=0
  )

writer.close()

# Commented out IPython magic to ensure Python compatibility.
# Let's view the graph using tensorboard
